// Sélectionneur de langue
const selector = document.getElementById('language-selector');

// Fonction de traduction continue
function translatePage(lang) {
  document.querySelectorAll('[data-key]').forEach(element => {
    const key = element.getAttribute('data-key');
    if (translations[lang][key]) {
      element.innerHTML = translations[lang][key]; // innerHTML pour conserver les spans
    }
  });
  localStorage.setItem('lang', lang);
}

// Événement de changement de langue
selector.addEventListener('change', e => translatePage(e.target.value));

// Initialisation selon la langue sauvegardée
const savedLang = localStorage.getItem('lang') || 'fr';
selector.value = savedLang;
translatePage(savedLang);

